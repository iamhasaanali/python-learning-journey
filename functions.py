def subtract(number1, number2):
    result = number1 - number2
    return result
answer = subtract(15, 5)
print(answer)

def multiply(number1, number2):
    result = number1 * number2
    return result
answer = multiply(10, 5)
print(answer)

def devide(number1, number2):
    result = number1 / number2
    return result
answer = devide(10, 5)
print(answer)